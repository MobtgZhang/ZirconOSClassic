//! 架构无关帧缓冲控制台（x86_64 / aarch64 GRE 共用）。
//! Framebuffer console driver with embedded 8x16 bitmap font.
//! Used as a fallback when VGA text mode (0xB8000) is not available,
//! i.e. when the bootloader sets up a graphical (VBE/GOP) framebuffer.

const CHAR_W: usize = 8;
const CHAR_H: usize = 16;

var fb_addr: usize = 0;
var fb_pitch: usize = 0;
var fb_width: usize = 0;
var fb_height: usize = 0;
var fb_bpp: usize = 0;
var cols: usize = 0;
var rows: usize = 0;
var cur_col: usize = 0;
var cur_row: usize = 0;
var ready: bool = false;
var console_enabled: bool = true;

/// Multiboot2 RGB framebuffer: false = RGB byte order; true = BGR (QEMU typical).
var fb_order_bgr: bool = true;

var fg_color: u32 = 0x00AAAAAA;
var bg_color: u32 = 0x00000000;

pub fn setConsoleEnabled(e: bool) void {
    console_enabled = e;
}

pub fn isConsoleEnabled() bool {
    return console_enabled;
}

pub fn init(addr: usize, w: u32, h: u32, pitch: u32, bpp: u8) void {
    initEx(addr, w, h, pitch, bpp, true);
}

pub fn initEx(addr: usize, w: u32, h: u32, pitch: u32, bpp: u8, order_bgr: bool) void {
    fb_addr = addr;
    fb_width = w;
    fb_height = h;
    fb_pitch = pitch;
    fb_bpp = bpp;
    fb_order_bgr = order_bgr;
    cols = w / CHAR_W;
    rows = h / CHAR_H;
    cur_col = 0;
    cur_row = 0;
    ready = true;
}

pub fn screenWidth() usize {
    return fb_width;
}

pub fn screenHeight() usize {
    return fb_height;
}

pub fn setTextColors(fg: u32, bg: u32) void {
    fg_color = fg;
    bg_color = bg;
}

fn packPixel32(color: u32) u32 {
    const r: u8 = @truncate(color >> 16);
    const g: u8 = @truncate(color >> 8);
    const b: u8 = @truncate(color);
    if (fb_order_bgr) {
        return @as(u32, b) | (@as(u32, g) << 8) | (@as(u32, r) << 16);
    }
    return @as(u32, r) | (@as(u32, g) << 8) | (@as(u32, b) << 16);
}

pub fn fillRect(x0: usize, y0: usize, rw: usize, rh: usize, color: u32) void {
    if (!ready) return;
    const ymax = @min(y0 + rh, fb_height);
    const xmax = @min(x0 + rw, fb_width);
    if (x0 >= fb_width or y0 >= fb_height or xmax <= x0 or ymax <= y0) return;

    // 勿用 *volatile u32 整块写：若 fb_pitch 非 4 倍数，y*fb_pitch+x*4 可能非 4 对齐，
    // Zig Debug 下 @ptrFromInt → *u32 会 panic「incorrect alignment」。
    if (fb_bpp == 32) {
        var y: usize = y0;
        while (y < ymax) : (y += 1) {
            var x: usize = x0;
            while (x < xmax) : (x += 1) {
                putPixel(x, y, color);
            }
        }
        return;
    }

    var y: usize = y0;
    while (y < ymax) : (y += 1) {
        var x: usize = x0;
        while (x < xmax) : (x += 1) {
            putPixel(x, y, color);
        }
    }
}

pub fn isReady() bool {
    return ready;
}

pub fn clear() void {
    if (!ready or !console_enabled) return;
    const total_bytes = fb_pitch * fb_height;
    const ptr: [*]volatile u8 = @ptrFromInt(fb_addr);
    var i: usize = 0;
    while (i < total_bytes) : (i += 1) {
        ptr[i] = 0;
    }
    cur_col = 0;
    cur_row = 0;
}

pub fn write(s: []const u8) void {
    if (!ready or !console_enabled) return;
    for (s) |c| putChar(c);
}

fn putChar(c: u8) void {
    switch (c) {
        '\n' => newLine(),
        '\r' => {
            cur_col = 0;
        },
        '\t' => {
            const next = (cur_col + 8) & ~@as(usize, 7);
            cur_col = if (next < cols) next else cols;
        },
        else => {
            if (cur_col >= cols) newLine();
            drawGlyph(cur_col, cur_row, c);
            cur_col += 1;
        },
    }
}

fn newLine() void {
    cur_col = 0;
    if (cur_row + 1 < rows) {
        cur_row += 1;
        return;
    }
    scroll();
}

fn scroll() void {
    const line_bytes = fb_pitch * CHAR_H;
    const total_lines = rows * CHAR_H;
    const src: [*]const volatile u8 = @ptrFromInt(fb_addr + line_bytes);
    const dst: [*]volatile u8 = @ptrFromInt(fb_addr);

    var i: usize = 0;
    const copy_bytes = fb_pitch * (total_lines - CHAR_H);
    while (i < copy_bytes) : (i += 1) {
        dst[i] = src[i];
    }

    // Clear the last row
    const last_row_start = fb_pitch * (total_lines - CHAR_H);
    i = 0;
    while (i < line_bytes) : (i += 1) {
        dst[last_row_start + i] = 0;
    }
}

fn drawGlyph(col: usize, row: usize, ch: u8) void {
    drawGlyphAt(col * CHAR_W, row * CHAR_H, ch);
}

/// 在像素坐标绘制 8×16 文本（不移动控制台光标）。
pub fn drawTextAtPx(px: usize, py: usize, fg: u32, bg: u32, s: []const u8) void {
    if (!ready) return;
    const old_fg = fg_color;
    const old_bg = bg_color;
    fg_color = fg;
    bg_color = bg;
    defer {
        fg_color = old_fg;
        bg_color = old_bg;
    }
    var col: usize = 0;
    for (s) |c| {
        drawGlyphAt(px + col * CHAR_W, py, c);
        col += 1;
    }
}

fn drawGlyphAt(base_x: usize, base_y: usize, ch: u8) void {
    const glyph = getGlyph(ch);
    var y: usize = 0;
    while (y < CHAR_H) : (y += 1) {
        const bits = glyph[y];
        var x: usize = 0;
        while (x < CHAR_W) : (x += 1) {
            const on = (bits >> @intCast(7 - x)) & 1;
            const color: u32 = if (on != 0) fg_color else bg_color;
            putPixel(base_x + x, base_y + y, color);
        }
    }
}

pub fn putPixel(x: usize, y: usize, color: u32) void {
    if (x >= fb_width or y >= fb_height) return;
    const bytes_pp = fb_bpp / 8;
    const offset = y * fb_pitch + x * bytes_pp;
    const ptr: [*]volatile u8 = @ptrFromInt(fb_addr + offset);
    const r: u8 = @truncate(color >> 16);
    const g: u8 = @truncate(color >> 8);
    const b: u8 = @truncate(color);

    if (bytes_pp >= 4) {
        if (fb_order_bgr) {
            ptr[0] = b;
            ptr[1] = g;
            ptr[2] = r;
        } else {
            ptr[0] = r;
            ptr[1] = g;
            ptr[2] = b;
        }
        if (bytes_pp == 4) ptr[3] = 0;
    } else if (bytes_pp == 3) {
        if (fb_order_bgr) {
            ptr[0] = b;
            ptr[1] = g;
            ptr[2] = r;
        } else {
            ptr[0] = r;
            ptr[1] = g;
            ptr[2] = b;
        }
    } else if (bytes_pp == 2) {
        // Convert 24-bit to RGB565
        const r5: u16 = @truncate((color >> 19) & 0x1F);
        const g6: u16 = @truncate((color >> 10) & 0x3F);
        const b5: u16 = @truncate((color >> 3) & 0x1F);
        const c16: u16 = (r5 << 11) | (g6 << 5) | b5;
        ptr[0] = @truncate(c16);
        ptr[1] = @truncate(c16 >> 8);
    }
}

/// bpp32 下整字写入，第 4 字节为 0xFF（`putPixel` 写 0；少数 GOP/合成路径会按 alpha 处理）。
pub fn putPixelXrgbOpaque(x: usize, y: usize, color: u32) void {
    if (x >= fb_width or y >= fb_height) return;
    if (fb_bpp != 32) {
        putPixel(x, y, color);
        return;
    }
    const offset = y * fb_pitch + x * 4;
    const ptr: [*]volatile u8 = @ptrFromInt(fb_addr + offset);
    const px = packPixel32(color) | 0xFF000000;
    ptr[0] = @truncate(px);
    ptr[1] = @truncate(px >> 8);
    ptr[2] = @truncate(px >> 16);
    ptr[3] = @truncate(px >> 24);
}

/// 读回 **0xRRGGBB**（与 `putPixel` 一致）；仅 bpp32 有效。
pub fn getPackedRgbAt(x: usize, y: usize) u32 {
    if (!ready or x >= fb_width or y >= fb_height) return 0;
    if (fb_bpp != 32) return 0;
    const offset = y * fb_pitch + x * 4;
    const ptr: [*]const volatile u8 = @ptrFromInt(fb_addr + offset);
    if (fb_order_bgr) {
        const b = ptr[0];
        const g = ptr[1];
        const r = ptr[2];
        return (@as(u32, r) << 16) | (@as(u32, g) << 8) | @as(u32, b);
    }
    const r = ptr[0];
    const g = ptr[1];
    const b = ptr[2];
    return (@as(u32, r) << 16) | (@as(u32, g) << 8) | @as(u32, b);
}

fn getGlyph(ch: u8) *const [16]u8 {
    if (ch >= 32 and ch < 127) {
        return &font_8x16[ch - 32];
    }
    return &font_8x16[95]; // solid block fallback
}

